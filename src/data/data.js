/**
 * Maps a TripAdvisor location object to the app's destination structure.
 */
export const mapApiToDestination = (apiLoc) => ({
  name: apiLoc.name || apiLoc.localizedName,
  image: apiLoc.image?.url || apiLoc.thumbnail?.url || 'https://images.unsplash.com/photo-1488646953014-85cb44e25828?auto=format&fit=crop&w=900&q=80',
  description: apiLoc.description || `Explore the beautiful city of ${apiLoc.name || apiLoc.localizedName || ''}`,
  price: apiLoc.price || 'Check Live Rates',
  locationId: apiLoc.locationId
});

/**
 * Maps a TripAdvisor hotel object to the app's hotel structure.
 */
export const mapApiToHotel = (apiHotel) => ({
  id: apiHotel.hotelId,
  name: apiHotel.title,
  image: apiHotel.bubbleRating?.image || 'https://images.unsplash.com/photo-1566073771259-6a8506099945?auto=format&fit=crop&w=900&q=80',
  rating: parseFloat(apiHotel.bubbleRating?.rating) || 4.5,
  reviews: apiHotel.bubbleRating?.numberReviews || '1,000+',
  price: apiHotel.priceForDisplay || '₹8,500',
  description: apiHotel.title + ' offers a premium stay experience.',
  amenities: ['Free WiFi', 'Pool', 'Spa', 'Restaurant'],
  location: apiHotel.distanceFromCenter || 'Center City'
});
